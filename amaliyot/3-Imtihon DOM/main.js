let boxElement = document.querySelector(".card");
yangiliklar.forEach((val) => {

    let row = document.querySelector(".row");

    let card = document.createElement("div");
    card.classList.add("card");
    card.classList.add("col-3");
    card.classList.add("p-3");
    card.classList.add("m-3");
    card.classList.add("border-3");

    let imgElement = document.createElement("img");
    imgElement.src = val.photo;
    imgElement.classList.add("card-img-top");

    let titleElement = document.createElement("h5");
    titleElement.innerText = val.title;
    titleElement.classList.add("card-title");
    titleElement.style.margin = "20px 0";
    
    let timeElement = document.createElement("p");
    timeElement.innerHTML = val.date;
    timeElement.classList.add("card-text");

    categoryElement = document.createElement("p");
    categoryElement.innerHTML = val.categories;
    categoryElement.classList.add("category");

    card.append(imgElement);
    card.append(titleElement);
    card.append(timeElement);
    card.append(categoryElement);

    row.append(card);

})



